    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        try {
            
            String query = "UPDATE sql2390162.Result SET Religion = ?,Sinhala = ?,English = ?,Mathematics = ?,Science = ? WHERE StudentNo =?";
            pst = con.prepareStatement(query);
            
            pst.setInt(1,Integer.valueOf(Religion.getText()));
            pst.setInt(2,Integer.valueOf(Sinhala.getText()));
            pst.setInt(3,Integer.valueOf(English.getText()));
            pst.setInt(4,Integer.valueOf(Mathematics.getText()));
            pst.setInt(5,Integer.valueOf(Science.getText()));
            pst.setString(6,Index.getText());
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this,"Update succefull !");
            this.dispose();
            new Edit_Results().setVisible(true);
            
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this,"Error : "+ex);
        }
    }//GEN-LAST:event_UpdateActionPerformed

